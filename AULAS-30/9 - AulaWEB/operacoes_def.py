def calc_soma (num1, num2):
    soma = num1 + num2
    return soma

def calc_sub (num1, num2):
    subtracao = num1 - num2
    return subtracao

def calc_mult (num1, num2):
    multiplicacao = num1 * num2
    return multiplicacao

def calc_div1 (num1, num2):
    div_int = num1 // num2
    return div_int

def calc_div2 (num1, num2):
    div_fra = num1 / num2
    return div_fra

def calc_resto (num1, num2):
    resto = num1 % num2
    return resto

def calc_raiz (num1, num2):
    raiz = num1 ** (1/num2)
    return raiz
